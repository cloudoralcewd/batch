# PYTHON'S BUILT-IN DATA TYPES

# 1. Numbers: Integer and Float
age = 25 # Integer
temapareture = 36.5 # Float

# 2. Boolean: Logical values => True or False
print(age == 35)
print(age < 30)

# 3. Strings: Sequence of characters
model_name = 'ChatGPT'
print(model_name)
print(type(model_name))

# 4. List: Ordered, mutable sequences of objects.
person = [ "RNS", 15, False, 3.14, "10th", 15 ]
print(len(person))
print(type(person))

# 5. Tuple: Ordered, immutable sequences of objects.
coordinates = ( 10, 20, 10 )
print(len(coordinates))
print(type(coordinates))

# 6. Set: Unordered collections of unique elements.
unique_numbers = {1, 2, 3, 4, 5, 5, 6, 4}
print(unique_numbers)
print(len(unique_numbers))
print(type(unique_numbers))

# 7. Dictionary: Unordered collections of key-value pairs.
employee = {
    "name": "RNS",
    "age": 13,
    "is_student": True
}

print(employee)
print(len(employee))
print(type(employee))






