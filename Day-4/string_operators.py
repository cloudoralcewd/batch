# PYTHON STRING BASICS

# Defining string variables (use '' or "" but stay consistent)

model1_summary = 'This AI model predicts stock trends.'
prediction_message = "AI will revolutionize industries!"

# Printing a basic string
print(model1_summary)
print(prediction_message)

# Special Characters @ # ! % $ | \ /
feedback = 'Press the \\ button to continue.'
print(feedback)

feedback1 = "Press the \\ button to continue."
print(feedback1)

# AI says, I'm here to assist you.
message = 'AI says, I\'m here to assist you.'
print(message)

message1 = "AI says, I'm here to assist you."
print(message1)

# Using a multi-line string '''    '''

ai_response = '''
Hello there!
I’m an AI here to help.
Feel free to ask me anything.
'''
print(ai_response)

welcome_msg = ("Welcome to AI Bot! \n Your virtual assistant \n here to assist \n with all things tech!")
print(welcome_msg)

gen_ai_message = ('Generative AI has revolutionized the way we create from text generation to image synthesis.'
                  'It is like having a creative partner!')
print(gen_ai_message)

# Printing an emoji for fun. Python strings are UTF-8 encoded.
print('🤖')


